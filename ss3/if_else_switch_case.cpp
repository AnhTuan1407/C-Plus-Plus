#include <bits/stdc++.h>
using namespace std;

//abs(x): gia tri tuyet doi
//pow(a, b): tinh a^b -> tra ve double
//sqrt(x): tinh can bac 2 -> tra ve double
//round(x): lam tron so
int main() {
	int a = -100;
	cout << abs(a) << endl;
	
	int b = 2, c = 10;
	cout << pow(b, c) << endl; // b^c
	
	int d = 16;
	cout << sqrt(d) << endl;
	
	float e =  2.5632;
	cout << round(e) << endl;
return 0;
} 
